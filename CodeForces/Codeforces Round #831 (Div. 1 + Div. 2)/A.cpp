#include <bits/stdc++.h>
using namespace std;

void work () {
    int n;
    cin >> n;
    if (n == 2) {
        cout << 7 << '\n';
        return;
    }
    cout << 3 << '\n';
}

int main () {
    cin.tie(0), cout.tie(0);
    ios::sync_with_stdio(false);
    int T;
    cin >> T;
    while (T--) {
        work();
    }
    return 0;
}